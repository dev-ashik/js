var age = 23;
console.log("Number:", age); //Number

var stringAge = age.toString()
console.log(stringAge, "is", typeof(stringAge)); //String

var string2 = String(age);
console.log(string2, "is", typeof(string2)); //String

var string3 = age + "";
console.log(string3, "is", typeof(string3)); //String

// boolean to string
var sick = true;
console.log("boolean to string:", sick.toString); // String

