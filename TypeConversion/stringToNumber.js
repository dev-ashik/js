// string to number
var salary = "50000";
console.log("string salary:", salary);

var numberSalary = Number(salary);
console.log("number salary:", numberSalary);

var numberSalary2 = Number.parseInt(salary);
console.log("int number salary2:", numberSalary2);



//Number to parse float
var salary2 = "50000.500";
console.log("salary2:", salary2);

var floatSalary = Number.parseFloat(salary2);
console.log("float Salary:", floatSalary);
