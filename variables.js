// Variables
// JavaScript is a case Sensitive language

var name = "Md Ashik Mahmud"; // string
console.log("Name: ", name);

var age = 23; // int
console.log("age", age);

var weight = 75.4; // float
console.log("weight: ", weight);

var married = false; // boolean
console.log("married: ", married);

